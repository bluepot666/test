void xdra_imct_orginal_c(float *in, float *out, int M)
{

	int N = M << 1;
	for(int n = 0; n < N; n++)
	{
		float sum = 0.0;
		for(int k = 0; k < M; k++)
		{
			float t0 = (2 * k + 1) * (float)M_PI/ (2 * M);
			float t1 = n + (M + 1)/2.0f;
			sum += in[k] * cos( t0 * t1);
		}
		out[n] = sum;	
	}

}

